package hashutil

// FNV1a64 returns a 64-bit hash value for the given string using the FNV-1a algorithm.
// Suitable for short strings with low collision probability.
func FNV1a64(s string) uint64 {
	const (
		offset = 14695981039346656037
		prime  = 1099511628211
	)
	h := uint64(offset)
	for i := 0; i < len(s); i++ {
		h ^= uint64(s[i])
		h *= prime
	}
	return h
}
